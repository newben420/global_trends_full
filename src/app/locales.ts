export function getMeta(
    keyPath: string,
    params: Record<string, string> = {},
    lang: string = LOCALES[0]
): string {
    // Find language, fallback to English if missing
    const langData = META_TRANSLATIONS[lang] || META_TRANSLATIONS[LOCALES[0]];
    if (!langData) return keyPath;

    // Resolve nested key path (e.g., "HOME.TITLE")
    const parts = keyPath.split('.');
    let value: any = langData;
    for (const part of parts) {
        value = value?.[part];
        if (value === undefined) return keyPath;
    }

    if (typeof value !== 'string') return keyPath;

    // Simple placeholder replacement
    return value.replace(/{{(.*?)}}/g, (_, match) => {
        const trimmedKey = match.trim();
        return params[trimmedKey] ?? `{{${trimmedKey}}}`;
    });
}

export const metaTrans = (lang: string, keys: string[], params: any[]) => keys.map((key, i) => getMeta(key, params[i], lang));